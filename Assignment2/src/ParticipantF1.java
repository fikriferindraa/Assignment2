

public class ParticipantF1
{
	String Driver;
	String Team;
	String Origin;
	String Point;
	
	public ParticipantF1(String Driver, String Team, String Origin, String Point)
	{
		this.Driver = Driver;
		this.Origin = Origin;
		this.Team = Team;
		this.Point = Point;
	}
	
	public String setDriver()
	{
		return Driver;
	}
	
	public String setTeam()
	{
		return Team;
	}
	
	public String setOrigin()
	{
		return Origin;
	}
	
	public String setPoint()
	{
		return Point;
	}
}
	

